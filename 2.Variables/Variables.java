class Variables {
    public static void main(String args[]) {
        int a = 10;
        int b = 20;
        int result = a + b;
        int p = 2, q = 3, r = 10;
        int x, y, z;
        x = y = z = 100;
        System.out.println(x + y + z); // 300
        System.out.println(p + q + r); // 15
        System.out.println(result); // 30
        System.out.println("Java is everything!"); // Java is everything!
    }
}